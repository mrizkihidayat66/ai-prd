import type { Plan, Project } from '@/generated/prisma/client';
import {
  extractProblemStatement,
  extractTechStack,
  extractTopRules,
  extractKeyEntities,
} from './plan-extractor';

export function buildInitAiMd(project: Project, plan: Plan): string {
  const exportDate = new Date().toISOString().split('T')[0];
  const version = plan.version ?? 1;

  const problemStatement = extractProblemStatement(plan.prd);
  const techSummary = extractTechStack(plan.architecture);
  const topRules = extractTopRules(plan.rules);
  const keyEntities = extractKeyEntities(plan.dbSchema);

  return [
    `# ${project.name}`,
    '',
    `> Generated by **init-ai** · ${exportDate} · Plan v${version}`,
    project.description ? `> ${project.description}` : '',
    '',
    '---',
    '',
    '## 🚀 Quick Start for AI Agents',
    '',
    '1. Read this file for context and orientation',
    '2. Check `.init-ai/initial-plan/task.md` for the current task list',
    '3. Follow the rules in `.init-ai/workspace-config/rules.md` at all times',
    '4. Use the skills in `.init-ai/workspace-config/skills/` to work efficiently',
    '',
    '---',
    '',
    '## 📋 Project Overview',
    '',
    problemStatement,
    '',
    '→ Full requirements: `.init-ai/initial-plan/prd.md`',
    '',
    '---',
    '',
    '## 🏗️ Architecture at a Glance',
    '',
    techSummary,
    '',
    '→ Architecture: `.init-ai/initial-plan/architecture.md`',
    '→ Diagrams: `.init-ai/initial-plan/diagrams.md`',
    '→ API spec: `.init-ai/initial-plan/api-spec.md`',
    '→ Effort estimate: `.init-ai/initial-plan/effort-estimate.md`',
    '',
    '---',
    '',
    '## 📦 Core Data Model',
    '',
    keyEntities,
    '',
    '→ Full schema: `.init-ai/initial-plan/db-schema.md`',
    '',
    '---',
    '',
    '## 🔑 Critical Rules (Must Follow)',
    '',
    topRules,
    '',
    '→ Full ruleset: `.init-ai/workspace-config/rules.md`',
    '→ Cursor: `.init-ai/workspace-config/.cursorrules`',
    '→ Copilot: `.init-ai/workspace-config/copilot-instructions.md`',
    '',
    '---',
    '',
    '## ✅ Task List',
    '',
    'Check off tasks as you implement. Always sync after completing work.',
    '',
    '→ `.init-ai/initial-plan/task.md`',
    '→ Workflow: `.init-ai/initial-plan/workflow.md`',
    '',
    '---',
    '',
    '## 🤖 Specialized Agents',
    '',
    'Launch a specialized agent for role-focused work:',
    '',
    '| Agent | Role | File |',
    '|-------|------|------|',
    '| **Backend** | API routes, DB models, business logic | `.init-ai/workspace-config/agents/backend.md` |',
    '| **Frontend** | UI components, styling, client state | `.init-ai/workspace-config/agents/frontend.md` |',
    '| **Testing** | Unit tests, integration tests, QA | `.init-ai/workspace-config/agents/testing.md` |',
    '| **Architect** | Design reviews, refactoring, decisions | `.init-ai/workspace-config/agents/architect.md` |',
    '',
    '---',
    '',
    '## ⚡ Skills',
    '',
    'Reusable step-by-step workflows compatible with Claude Code, Antigravity, and OpenCode:',
    '',
    '| Skill | Purpose |',
    '|-------|---------|',
    '| `implement-feature` | Build a new feature end-to-end from task to tested code |',
    '| `write-tests` | Add comprehensive tests for any module or feature |',
    '| `review-pr` | Code review against project rules and architecture |',
    '| `update-progress` | Log progress and check off completed tasks |',
    '',
    'Skills location: `.init-ai/workspace-config/skills/`',
    '',
    '---',
    '',
    '## 📁 Reference Index',
    '',
    '| Document | Path |',
    '|----------|------|',
    '| Product Requirements | `.init-ai/initial-plan/prd.md` |',
    '| Architecture | `.init-ai/initial-plan/architecture.md` |',
    '| **Task List ← start here** | `.init-ai/initial-plan/task.md` |',
    '| API Specification | `.init-ai/initial-plan/api-spec.md` |',
    '| Database Schema | `.init-ai/initial-plan/db-schema.md` |',
    '| Development Workflow | `.init-ai/initial-plan/workflow.md` |',
    '| System Diagrams | `.init-ai/initial-plan/diagrams.md` |',
    '| Effort Estimate | `.init-ai/initial-plan/effort-estimate.md` |',
    '| Coding Rules | `.init-ai/workspace-config/rules.md` |',
    '| Project Metadata | `.init-ai/project.json` |',
    '',
    '---',
    '',
    '> **AI Agent Note**: After completing work, use the `update-progress` skill or manually',
    '> append a note to `.init-ai/context/progress.md` and check off tasks in',
    '> `.init-ai/initial-plan/task.md`.',
  ]
    .filter((line) => line !== null)
    .join('\n');
}
